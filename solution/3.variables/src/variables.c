#include "variables.h"
#include <stdio.h>

void run_variables()
{
    int first_num  = 4;
    int second_num = 2;

    printf("Numeros: %d e %d\n", first_num, second_num);
    printf("Soma: %d\n", first_num + second_num);
    printf("Diferenca: %d\n", first_num - second_num);
    printf("Produto: %d\n", first_num * second_num);
    printf("Quociente: %d\n", first_num / second_num);
}
